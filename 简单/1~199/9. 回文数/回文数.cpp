#include <iostream>
#include <string>
#include <cstring>
#include <algorithm>
#include "../check/CheckResult.h"

using namespace std;

class Solution {
public:
    bool isPalindrome(int x) {
        if (x < 0) {
            return false;
        }

        string s = to_string(x);
        reverse(s.begin(), s.end());
        long long longdata = stoll(s);

        return (longdata == x);
    }
};

int main()
{
    Solution o;
    CheckResult check;
    check.checkBool(true, o.isPalindrome(121));
    check.checkBool(false, o.isPalindrome(-121));
    check.checkBool(false, o.isPalindrome(10));
}

