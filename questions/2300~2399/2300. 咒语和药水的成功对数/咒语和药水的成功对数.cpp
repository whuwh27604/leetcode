#include <iostream>
#include <algorithm>
#include "../check/CheckResult.h"

using namespace std;

class Solution {
public:
    vector<int> successfulPairs(vector<int>& spells, vector<int>& potions, long long success) {
        int n = spells.size(), m = potions.size();
        vector<int> pairs(n);

        sort(potions.begin(), potions.end());

        for (int i = 0; i < n; ++i) {
            long long potion = success / spells[i];

            if ((success % spells[i]) != 0) {
                potion += 1;
            }

            if (potion > potions.back()) {
                pairs[i] = 0;
            }
            else {
                pairs[i] = distance(lower_bound(potions.begin(), potions.end(), (int)potion), potions.end());
            }
        }

        return pairs;
    }
};

int main()
{
    CheckResult check;
    Solution o;

    vector<int> spells = { 5,1,3 };
    vector<int> potions = { 1,2,3,4,5 };
    vector<int> actual = o.successfulPairs(spells, potions, 7);
    vector<int> expected = { 4,0,3 };
    check.checkIntVector(expected, actual);

    spells = { 3,1,2 };
    potions = { 8,5,8 };
    actual = o.successfulPairs(spells, potions, 16);
    expected = { 2,0,2 };
    check.checkIntVector(expected, actual);
}

