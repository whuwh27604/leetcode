#include <iostream>
#include "../check/CheckResult.h"

using namespace std;

class Solution {
public:
    long long waysToBuyPensPencils(int total, int cost1, int cost2) {
        long long ways = 0;

        for (int pens = 0; pens <= total / cost1; ++pens) {
            ways += ((total - (long long)cost1 * pens) / cost2 + 1);
        }

        return ways;
    }
};

int main()
{
    CheckResult check;
    Solution o;

    check.checkLongLong(9, o.waysToBuyPensPencils(20, 10, 5));
    check.checkLongLong(1, o.waysToBuyPensPencils(5, 10, 10));
}

