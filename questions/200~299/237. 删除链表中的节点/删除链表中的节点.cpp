#include <iostream>
#include "../check/CheckResult.h"

using namespace std;

class Solution {
public:
    void deleteNode(ListNode* node) {
        node->val = node->next->val;
        node->next = node->next->next;
    }
};

int main()
{
    Solution o;
    CheckResult check;

    vector<int> values = { 4,5,1,9 };
    ListNode* actual = createList(values);
    o.deleteNode(actual->next);
    values = { 4,1,9 };
    check.checkSingleList(createList(values), actual);
}

