#include <iostream>
#include "../check/CheckResult.h"

using namespace std;

class Solution {
public:
    vector<int> numberOfLines(vector<int>& widths, string S) {
        int widthSum = 0;
        int rows = 1;

        for (unsigned int i = 0; i < S.size(); i++) {
            int curWidth = widths[S[i] - 'a'];
            if ((widthSum + curWidth) > 100) {
                rows++;
                widthSum = curWidth;
            }
            else {
                widthSum += curWidth;
            }
        }

        return { rows, widthSum };
    }
};

int main()
{
    Solution o;
    CheckResult check;

    vector<int> widths = { 10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10 };
    string S = "abcdefghijklmnopqrstuvwxyz";
    vector<int> actual = o.numberOfLines(widths, S);
    vector<int> expected = { 3, 60 };
    check.checkIntVector(expected, actual);

    widths = { 4,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10 };
    S = "bbbcccdddaaa";
    actual = o.numberOfLines(widths, S);
    expected = { 2, 4 };
    check.checkIntVector(expected, actual);

    widths = { 4,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10,10 };
    S = "a";
    actual = o.numberOfLines(widths, S);
    expected = { 1, 4 };
    check.checkIntVector(expected, actual);
}

